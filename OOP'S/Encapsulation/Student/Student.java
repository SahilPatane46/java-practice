class Student{
//States
private int sid;
private String name;

//Behaviour
//Getter method to get sid
public int getsid()
{
	return sid;
}

//Getter method to get name
public String getname()
{
	return name;
}

//Setter method to set sid
public void setsid(int sid)
{
	this.sid=sid;
}

//Setter method to set name
public void setname(String name)
{
	this.name=name;
}
}