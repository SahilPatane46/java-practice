class Switch5{
	public static void main(String[] args) {
	  int a = 10;         //switch without break
	  switch(a)
		{
		case 10:{
		System.out.println(10);
		}
		case 20:{
		System.out.println(20);
		}
		case 30:{
		System.out.println(30);
		}
		default:{
		System.out.println("default");
		}
	  }
    }	
}