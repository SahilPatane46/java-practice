class compound_assisgnment1
{
	public static void main(String[] args)
	{
		double balance = 10000;
		System.out.println(balance);

		//Deposit 2000
		//balance=balance+2000;
		balance+=2000;
		System.out.println(balance);

		//withdrawal 5000
		//balance=balance-5000;
		balance-=5000;
		System.out.println(balance);

		//double amount
		balance*=2;
		System.out.println(balance);
	}
}